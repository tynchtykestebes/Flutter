class Continents {
  final String name;
  final String image;

  const Continents({
    required this.name,
    required this.image,
  });
}

const Continents asia = Continents(name: 'Asia', image: 'asia');
const Continents europe = Continents(name: 'Europe', image: 'europe');
const Continents northAmerica =
    Continents(name: 'North America', image: 'north_america');
const Continents southAmerica =
    Continents(name: 'South America', image: 'south_america');
const Continents africa = Continents(name: 'Africa', image: 'africa');
const Continents australia = Continents(name: 'Australia', image: 'australia');
